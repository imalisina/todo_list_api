     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        // Define a variable to store number of tasks
        $taskCount = Task::count();
        if ($id <= $taskCount) {
            $selectedTask = Task::find($id);
        } else {
            return "Invalid task !";
        }
        return $selectedTask;
    }

